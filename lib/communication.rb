

class Communication

  def game_start
    puts  "Welcome to Tic Tac Toe\n" \
          "Take turns placing your X's\n" \
          "and O's\n" \
          "Use 1 thru 9 to choose your square!\n" \
          "1 will be top left, 2 top middle, and\n" \
          "continue so on until 9 which is bottom right!\n" \
          "Good Luck!!!!"
  end

  def your_turn_X
    puts "It's your turn X. Choose your Square!"
  end

  def your_turn_O
    puts "It's your turn O. Choose your Square!"
  end

end